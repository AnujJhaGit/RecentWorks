function findStopWords() {
    const inputText = document.getElementById('inputText').value.toLowerCase();
    const stopWordsTableBody = document.getElementById('stopWordsTableBody');
  
    const foundWords = {};
  
    const stopWordsArray = stopWordsList.split('|').map(word => word.trim());
  
    const foundStopWords = inputText.match(new RegExp('\\b(' + stopWordsArray.join('|') + ')\\b', 'gi'));
  
    if (foundStopWords) {
      foundStopWords.forEach(word => {
        const wordLC = word.toLowerCase().trim(); // Remove leading and trailing spaces
        if (wordLC.length > 0) { // Check if the word is not an empty string
          foundWords[wordLC] = (foundWords[wordLC] || 0) + 1;
        }
      });
  
      stopWordsTableBody.innerHTML = '';
  
      for (const word in foundWords) {
        const row = `<tr><td>${word}</td><td>${foundWords[word]}</td></tr>`;
        stopWordsTableBody.innerHTML += row;
      }
    } else {
      stopWordsTableBody.innerHTML = '<tr><td colspan="2">No stop words found.</td></tr>';
    }
  }
  
  
  function handleTextChange() {
    var autoUpdateCheckbox = document.getElementById('autoUpdateCheckbox');
  
    if (autoUpdateCheckbox.checked) {
      findStopWords();
    }
  }

